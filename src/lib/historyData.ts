export function getEraById(id: string): Era | undefined {
  return historyEras.find(e => e.id === id);
}

export function getEventById(eraId: string, eventId: string): HistoryEvent | undefined {
  const era = getEraById(eraId);
  return era?.events.find(e => e.id === eventId);
}
